# Basic for loop examples

# Example 1: Iterate through list
fruits = ["apple", "banana", "orange"]
for fruit in fruits:
    print(fruit)

# Example 2: Iterate through string
word = "Python"
for letter in word:
    print(letter)

# Example 3: Using range()
for i in range(5):
    print(i)

# Example 4: Range with start and end
for num in range(2, 8):
    print(num)

# Example 5: Range with step
for i in range(0, 10, 2):
    print(i)

# Example 6: Nested for loop
for i in range(3):
    for j in range(2):
        print(i, j)